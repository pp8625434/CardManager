package com.manage.money.model;


public class CreditResult {

	private String resultcode;
	
	private String msg;
	
	private String credit;
	
	private String money;
	
	private String thiscredit;//本次获得的积分
	
	private String thismoney;//本次金额
	
	private Integer code;

	public Integer getCode() {
		return code;
	}

	public void setCode(Integer code) {
		this.code = code;
	}

	public String getResultcode() {
		return resultcode;
	}

	public void setResultcode(String resultcode) {
		this.resultcode = resultcode;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getCredit() {
		return credit;
	}

	public void setCredit(String credit) {
		this.credit = credit;
	}

	public String getMoney() {
		return money;
	}

	public void setMoney(String money) {
		this.money = money;
	}

	public String getThiscredit() {
		return thiscredit;
	}

	public void setThiscredit(String thiscredit) {
		this.thiscredit = thiscredit;
	}

	public String getThismoney() {
		return thismoney;
	}

	public void setThismoney(String thismoney) {
		this.thismoney = thismoney;
	}

}
