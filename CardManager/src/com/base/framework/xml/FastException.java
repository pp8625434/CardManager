package com.base.framework.xml;

public class FastException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 4531652883539336529L;
	
	String errorMessage;
	 
    public FastException(String errorMessage)
    {
         this.errorMessage = errorMessage;
    }
 
    public String toString()
    {
         return errorMessage;
    }
 
    public String getMessage()
    {
         return errorMessage;
    }

	
}
