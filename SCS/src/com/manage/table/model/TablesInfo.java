package com.manage.table.model;

public class TablesInfo {
	
	private Integer id;
	
	private Integer tid;
	
	private String fieldName;
	
	private Integer fielType;
	
	private String fielDesc;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getTid() {
		return tid;
	}

	public void setTid(Integer tid) {
		this.tid = tid;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public Integer getFielType() {
		return fielType;
	}

	public void setFielType(Integer fielType) {
		this.fielType = fielType;
	}

	public String getFielDesc() {
		return fielDesc;
	}

	public void setFielDesc(String fielDesc) {
		this.fielDesc = fielDesc;
	}
	
	

}
