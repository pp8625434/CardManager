package com.base.framework.security.model;

import java.util.List;

public class PlatModel {
	
	Integer platcode;
	Integer userid;
	String token;
	List<PlatRole> list;
	public Integer getPlatcode() {
		return platcode;
	}
	public void setPlatcode(Integer platcode) {
		this.platcode = platcode;
	}
	public Integer getUserid() {
		return userid;
	}
	public void setUserid(Integer userid) {
		this.userid = userid;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public List<PlatRole> getList() {
		return list;
	}
	public void setList(List<PlatRole> list) {
		this.list = list;
	}

}
